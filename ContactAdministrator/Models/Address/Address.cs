namespace ContactAdministrator.Models.Address
{
  internal class Address
  {
    /// <summary>Gets or sets the street.</summary>
    /// <value>The street.</value>
    public string Street { get; set; }

    /// <summary>Gets or sets the house number.</summary>
    /// <value>The house number.</value>
    public string HouseNumber { get; set; }

    /// <summary>Gets or sets the city.</summary>
    /// <value>The city.</value>
    public string City { get; set; }

    /// <summary>Gets or sets the land.</summary>
    /// <value>The land.</value>
    public string Land { get; set; }

    /// <summary>Gets or sets the zip.</summary>
    /// <value>The zip.</value>
    public string Zip { get; set; }
  }
}